/** \brief Realiza una operacion de suma y devuelve el resultado
 * \param x es el primer operando de la suma
 * \param y es el segundo operando de la suma
 * \return El resultado de tipo flotante de la suma de las variables ingresadas.
 */
float fsumar(float x, float y);


/** \brief Realiza una operacion de resta y devuelve el resultado
 * \param x es el primer operando de la resta
 * \param y es el segundo operando de la resta
 * \return El resultado de tipo flotante de la resta de las variables ingresadas.
 */
float frestar(float x, float y);

/** \brief Realiza una operacion de division y devuelve el resultado
 * \param x es el dividendo
 * \param y es el divisor
 * \return El resultado de tipo flotante de la division de las variables ingresadas.
 */
float fdivision(float x, float y);

/** \brief Realiza una operacion de multiplicacion y devuelve el resultado
 * \param x es el primer operando de la multiplicacion
 * \param y es el segundo operando de la multiplicacion
 * \return El resultado de tipo flotante de la multiplicacion de las variables ingresadas.
 */
float fmultiplicacion(float x, float y);

/** \brief Realiza una operacion de factorial y devuelve el resultado
 * \param x es el numero al cual se realizara la operacion de factorial. El mismo solo puede ser 0 o cualquier numero natural.
 * \return El resultado de tipo entero del factorial de las variables ingresadas.
 */
int ffactorial(int x);

